    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        //EXCLUI NO BANCO
        int linha = jtFornecedor.getSelectedRow();
        int codigo = (int) jtFornecedor.getValueAt(linha, 0);
        
        if(controllerFornecedor.excluirFornecedorController(codigo)){
            JOptionPane.showMessageDialog(this, "Fornecedor Excluido com sucesso!","Atenção",JOptionPane.WARNING_MESSAGE);
            this.carregafornecedor();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao excluir o fornecedor!","Erro",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

