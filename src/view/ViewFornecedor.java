    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        salvarAlterar = "alterar";
        this.habilitarDesabilitarCampos(true);
        
        int linha = this.jtFornecedor.getSelectedRow();
        try{
            int codigo = (int) this.jtFornecedor.getValueAt(linha, 0);
            
            //recupera dados do banco
            modelFornecedor = controllerFornecedor.getFornecedorController(codigo);
            //seta nos campos
            this.jtfCodigo.setText(String.valueOf(modelFornecedor.getId_fornecedor()));
            this.jtfBairro.setText(modelFornecedor.getBairro_fornecedor());
            this.jtfCEP.setText(modelFornecedor.getCep_fornecedor());
            this.jtfCNPJ.setText(modelFornecedor.getCnpj_fornecedor());
            this.jtfCidade.setText(modelFornecedor.getCidade_fornecedor());
            this.jtfEnderco.setText(modelFornecedor.getEndereco_fornecedor());
            this.jtfNome.setText(modelFornecedor.getNome_fornecedor());
            this.jcbEstado.setSelectedItem(modelFornecedor.getUf_fornecedor());
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(this,"Erro ou não encontrado", "Aviso",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

    private boolean  validaCampos(){
        boolean Situ = true;
        
        if(jtfNome.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Nome é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(jtfBairro.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Bairro é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(jtfCEP.getText().equals("  .   -   ")){
          JOptionPane.showMessageDialog(this,"CEP é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(jtfCNPJ.getText().equals("  .   .   /    -  ")){
          JOptionPane.showMessageDialog(this,"CNPJ é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(jtfCidade.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Cidade é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(jtfEnderco.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Endereço é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(jcbEstado.getSelectedIndex() < 0){
          JOptionPane.showMessageDialog(this,"Estado é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
       
        return Situ;
        
    }
    
