    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        
        salvarAlterar = "alterar";
        this.habilitarDesabilitarCampos(true);
        
        int linha = this.jtUsuario.getSelectedRow();
        try{
            int codigo = (int) this.jtUsuario.getValueAt(linha, 0);
            
            //recupera dados do banco
            modelUsuario = controllerUsuario.getUsuarioController(codigo);
            //seta nos campos
            this.jtfCodigo.setText(String.valueOf(modelUsuario.getId_usuario()));
            this.jtfLoguin.setText(modelUsuario.getLogin_usuario());
            this.jtfNome.setText(modelUsuario.getNome_usuario());
            this.jtfPerfil.setText(String.valueOf(modelUsuario.getPerfil_usuario()));
            this.jtfDescPerfil.setText(modelUsuario.getNome_perfil());
            this.jtfSenha.setText("");
            this.jcbSituacao.setSelectedItem(modelUsuario.getSituacao_usuario());
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(this,"Erro ou não encontrado", "Aviso",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

