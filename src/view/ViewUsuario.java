    private void salvar() throws NoSuchAlgorithmException{
        //String password1MD5 = ConvertPasswordToMD5.convertPasswordToMD5(password1);
        modelUsuario.setLogin_usuario(this.jtfLoguin.getText());
        modelUsuario.setNome_usuario(this.jtfNome.getText());
        modelUsuario.setPerfil_usuario(Integer.parseInt(this.jtfPerfil.getText()));
        modelUsuario.setSenha_usuario(ConvertPasswordToMD5.convertPasswordToMD5(this.jtfSenha.getText()));
        modelUsuario.setSituacao_usuario(this.jcbSituacao.getSelectedItem().toString());
        
        if(controllerUsuario.salvarUsuarioController(modelUsuario) > 0){
            JOptionPane.showMessageDialog(this,"Usuario cadastrado com sucesso!","Atenção",JOptionPane.WARNING_MESSAGE);
            this.carregausuario();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
            
        } else{
            JOptionPane.showMessageDialog(this,"Erro ao cadastrar usuario produto!","Erro",JOptionPane.ERROR_MESSAGE);
        }   
    }
    
     private void alterar() throws NoSuchAlgorithmException{
        modelUsuario.setLogin_usuario(this.jtfLoguin.getText());
        modelUsuario.setNome_usuario(this.jtfNome.getText());
        modelUsuario.setPerfil_usuario(Integer.parseInt(this.jtfPerfil.getText()));
        modelUsuario.setSenha_usuario(ConvertPasswordToMD5.convertPasswordToMD5(this.jtfSenha.getText()));
        modelUsuario.setSituacao_usuario(this.jcbSituacao.getSelectedItem().toString());
        
        if(controllerUsuario.atualizarUsuarioController(modelUsuario)){
            JOptionPane.showMessageDialog(this,"Usuario alterado com sucesso!","Atenção",JOptionPane.WARNING_MESSAGE);
            this.carregausuario();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
            
        } else{
            JOptionPane.showMessageDialog(this,"Erro ao alterar usuario produto!","Erro",JOptionPane.ERROR_MESSAGE);
        }   
    }
    
     private boolean  validaCampos(){
        boolean Situ = true;
        
        if(jtfLoguin.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Loguin é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(jtfNome.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Nome é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(jtfSenha.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Senha é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(jtfPerfil.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Perfil é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(jcbSituacao.getSelectedIndex() < 0){
          JOptionPane.showMessageDialog(this,"Situação é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
       
        return Situ;
        
    }
    
    private void carregausuario(){
        listaModelUsuario = controllerUsuario.getListaUsuarioController();
        DefaultTableModel modelo = (DefaultTableModel) jtUsuario.getModel();
        modelo.setNumRows(0);
        
        //inserir produtos na tabela
        int cont = listaModelUsuario.size();
        for (int i = 0; i < cont; i++){
            modelo.addRow(new Object[]{
            listaModelUsuario.get(i).getId_usuario(),
            listaModelUsuario.get(i).getNome_usuario(),
            listaModelUsuario.get(i).getSituacao_usuario(),
            listaModelUsuario.get(i).getNome_perfil()
            });
    }
    }
    
    private void habilitarDesabilitarCampos(boolean situacao){
        jtfNome.setEditable(situacao);
        jtfPerfil.setEditable(situacao);
        jbPesqPerfil.setEnabled(situacao);
        jtfLoguin.setEditable(situacao);
        jtfSenha.setEditable(situacao);
        jcbSituacao.setEditable(situacao);
    }
    
    private void limparCampos(){
        jtfCodigo.setText("");
        jtfLoguin.setText("");
        jtfNome.setText("");
        jtfPerfil.setText("");
        jtfDescPerfil.setText("");
        jtfSenha.setText("");
        jcbSituacao.setSelectedIndex(0);
        
    }

