    private void jtfProdutoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtfProdutoFocusLost

       if(jtfProduto.getText().isEmpty()==false){
            //recupera dados do banco
            modelProduto = controllerProduto.getProdutoController(Integer.parseInt(jtfProduto.getText()));
            jtfDescProduto.setText(modelProduto.getDescricao_produto());
            if(jtfDescProduto.getText().isEmpty()){
               jtfProduto.setText("");
               JOptionPane.showMessageDialog(this,"Produto não encontrado!","Atenção",JOptionPane.WARNING_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_jtfProdutoFocusLost

    private boolean  validaCampos(){
        boolean Situ = true;
        
        if(jtfProduto.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Produto é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(formatado.converterVirgulaParaPontoDouble(jtfQtdAt.getText())<0){
          JOptionPane.showMessageDialog(this,"Quantidade Atual é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(formatado.converterVirgulaParaPontoDouble(jtfQtdMa.getText())<0){
          JOptionPane.showMessageDialog(this,"Quantidade Máxima é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
        if(formatado.converterVirgulaParaPontoDouble(jtfQtdMi.getText())<0){
          JOptionPane.showMessageDialog(this,"Quantidade Mínima é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
       
        return Situ;
        
    }
    
