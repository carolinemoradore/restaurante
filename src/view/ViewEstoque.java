    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        salvarAlterar = "alterar";
        this.habilitarDesabilitarCampos(true);
        
        int linha = this.jtEstoque.getSelectedRow();
        try{
            int codigo = (int) this.jtEstoque.getValueAt(linha, 0);
            
            //recupera dados do banco
            modelEstoque = controllerEstoque.getEstoqueController(codigo);
            //seta nos campos
            this.jtfCodigo.setText(String.valueOf(modelEstoque.getId_estoque()));
            this.jtfProduto.setText(String.valueOf(modelEstoque.getProduto_estoque()));
            this.jtfDescProduto.setText(modelEstoque.getDescricao_produto());
            this.jtfQtdAt.setText(String.valueOf(modelEstoque.getQtdatual_estoque()));
            this.jtfQtdMa.setText(String.valueOf(modelEstoque.getQtdemaxima_estoque()));
            this.jtfQtdMi.setText(String.valueOf(modelEstoque.getQtdeminima_estoque()));
        } catch (Exception e){
            JOptionPane.showMessageDialog(this,"Erro ou não encontrado", "Aviso",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

