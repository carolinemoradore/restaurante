    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        //EXCLUI NO BANCO
        int linha = jtEstoque.getSelectedRow();
        int codigo = (int) jtEstoque.getValueAt(linha, 0);
        
        if(controllerEstoque.excluirEstoqueController(codigo)){
            JOptionPane.showMessageDialog(this, "Estoque Excluido com sucesso!","Atenção",JOptionPane.WARNING_MESSAGE);
            this.carregaestoque();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao excluir o estoque","Erro",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

