    private void salvar(){
        modelPerfil.setNome_perfil(this.jtfNome.getText());
              
        if(controllerPerfil.salvarPerfilController(modelPerfil) > 0){
            JOptionPane.showMessageDialog(this,"Perfil cadastrado com sucesso!","Atenção",JOptionPane.WARNING_MESSAGE);
            this.carregaperfil();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
            
        } else{
            JOptionPane.showMessageDialog(this,"Erro ao cadastrar perfilo!","Erro",JOptionPane.ERROR_MESSAGE);
        }   
    }
    
     private void alterar(){
        modelPerfil.setNome_perfil(this.jtfNome.getText());
               
        if(controllerPerfil.atualizarPerfilController(modelPerfil)){
            JOptionPane.showMessageDialog(this,"Perfil alterado com sucesso!","Atenção",JOptionPane.WARNING_MESSAGE);
            this.carregaperfil();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
            
        } else{
            JOptionPane.showMessageDialog(this,"Erro ao alterar Perfil!","Erro",JOptionPane.ERROR_MESSAGE);
        }   
    }
    
    private boolean  validaCampos(){
        boolean Situ = true;
        
        if(jtfNome.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Nome é obrigatório","Atenção",JOptionPane.WARNING_MESSAGE);
          Situ = false;
        }
               
        return Situ;
        
    }
    
    private void carregaperfil(){
        listaModelPerfil = controllerPerfil.getListaPerfilController();
        DefaultTableModel modelo = (DefaultTableModel) jtUsuario.getModel();
        modelo.setNumRows(0);
        
        //inserir produtos na tabela
        int cont = listaModelPerfil.size();
        for (int i = 0; i < cont; i++){
            modelo.addRow(new Object[]{
            listaModelPerfil.get(i).getId_perfil(),
            listaModelPerfil.get(i).getNome_perfil()        
            });
    }
    }
    
    private void habilitarDesabilitarCampos(boolean situacao){
        jtfNome.setEditable(situacao);     
    }
    
    private void limparCampos(){
        jtfCodigo.setText("");
        jtfNome.setText("");
        
    }

